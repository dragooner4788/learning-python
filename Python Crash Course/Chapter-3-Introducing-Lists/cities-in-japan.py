cities_in_japan = ["tokyo", "osaka", "sendai",
                   "kyoto", "hiroshima", "nagasaki"]

cities_in_japan.sort()

print(cities_in_japan)

cities_in_japan.reverse()

print(cities_in_japan)

removed_city = cities_in_japan.pop(-1)

print(removed_city)

print(cities_in_japan)

cities_in_japan.sort(reverse=True)

print(cities_in_japan)

reversed(cities_in_japan)

print(cities_in_japan)

sorted(cities_in_japan)

print(cities_in_japan)
